package bm.cir.exceptions;

public class CIRSSyntaxException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public CIRSSyntaxException() {
	}

	public CIRSSyntaxException(String arg0) {
		super(arg0);
	}

	public CIRSSyntaxException(Throwable arg0) {
		super(arg0);
	}

	public CIRSSyntaxException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
